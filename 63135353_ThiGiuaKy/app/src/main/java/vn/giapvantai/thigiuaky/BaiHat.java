package vn.giapvantai.thigiuaky;

public class BaiHat {
    private String tenBai;
    private String tacGia;

    public BaiHat(String tenBai, String tacGia) {
        this.tenBai = tenBai;
        this.tacGia = tacGia;
    }

    public String getTenBai() {
        return tenBai;
    }

    public String getTacGia() {
        return tacGia;
    }

}
